package omw.model;

public class Skin {
	
	private Integer numeroSkin;
	private String nomSkin;
	private Integer price;
	private String imagePath;
	
	public Skin(){
	
	}

	public Integer getNumeroSkin() {
		return numeroSkin;
	}

	public void setNumeroSkin(Integer numeroSkin) {
		this.numeroSkin = numeroSkin;
	}

	public String getNomSkin() {
		return nomSkin;
	}

	public void setNomSkin(String nomSkin) {
		this.nomSkin = nomSkin;
	}

	public Integer getPrice() {
		return price;
	}

	public void setPrice(Integer price) {
		this.price = price;
	}

	public String getImagePath() {
		return imagePath;
	}

	public void setImagePath(String imagePath) {
		this.imagePath = imagePath;
	}

	
	
}
