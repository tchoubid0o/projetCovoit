package omw.model;

public class Ability {

		private String lettreAbility;
		private String nomAbility;
		private String imagePath;
		
		public Ability(){
			
		}		

		public String getLettreAbility() {
			return lettreAbility;
		}

		public void setLettreAbility(String lettreAbility) {
			this.lettreAbility = lettreAbility;
		}

		public String getNomAbility() {
			return nomAbility;
		}

		public void setNomAbility(String nomAbility) {
			this.nomAbility = nomAbility;
		}

		public String getImagePath() {
			return imagePath;
		}

		public void setImagePath(String imagePath) {
			this.imagePath = imagePath;
		}

		
		
}
